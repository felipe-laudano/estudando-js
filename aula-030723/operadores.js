const num1 = 5;
const num2 = 8;
const num3 = 4.5;

/**
 * Operadores Aritméticos
 * +: Adição
 * -: Subtração
 * *: Multiplicação
 * /: Divisão
 * **: Exponenciação
 * %: Resto da divisão
 */

let result; //inicialização da variavel

//adição
result = num1 + num2; //13
console.log(result);

//subtração
result = num1 - num2;
console.log(result);

//multiplicação
result = num1 * num2;
console.log(result);

//divisão
result = num1 / num2;
console.log(result);

//exponenciação
result = num1 ** num2;
console.log(result);

//resto da divisão
result = num2 % num3;
console.log(result);